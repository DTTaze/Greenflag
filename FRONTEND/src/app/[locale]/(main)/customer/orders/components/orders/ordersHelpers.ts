export const calculatePoints = (items: any[]) => {
  let total = 0;
  const wasteTypesMap: Record<string, number> = {
    paper: 10,
    plastic: 15,
    metal: 20,
    glass: 5,
    organic: 2,
  };
  items.forEach((item) => {
    total += (wasteTypesMap[item.type] || 5) * item.quantity;
  });
  return total;
};
